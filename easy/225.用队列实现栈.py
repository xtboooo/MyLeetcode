class MyStack:
    def __init__(self):
        import collections

        self.q = collections.deque()

    def push(self, x: int) -> None:
        n = len(self.q)
        self.q.append(x)
        for _ in range(n):
            self.q.appned(self.q.popleft())

    def pop(self) -> int:
        return self.q.popleft()

    def top(self) -> int:
        return self.q[0]

    def empty(self) -> bool:
        return not self.q


